/*
Escreva uma função que receba um número negativo e retorne um número positivo;
Dica: utilize a função Math.abs
*/

function retornarPositivo(a){
    return Math.abs(a);
}

console.log(retornarPositivo(-4));
console.log(retornarPositivo(-8));
console.log(retornarPositivo(3));
console.log(retornarPositivo(0));
console.log(retornarPositivo(-40.2));