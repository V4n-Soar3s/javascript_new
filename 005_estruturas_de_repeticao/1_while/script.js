/*

Estrutura de repetição: while

- Fará uma ação, até que a condição seja atingida;
- Precisamos realmente 'definir um fim' para o loop´, para não termos o problema de loop infinito;

*/

let y = 0;

while(y <= 20){
    console.log(y);
    y = y + 1;
}

