//Operador Ternário - boolean

/*

Boolean : operador ternário

 * Faz um comparativo em apenas uma linha de código;
 * console.log(true?1:2) - lê se "É true? se sim é 1 senão é 2"
 * console.log(false? 'falso':'verdadeiro')
 * Não é muito utilizado e pode deixar o código complicado de ler
 
 */

console.log(5 > 2 ? 'Sim' : 'Não');//aqui estamos lendo 5 é maior que 2? Se for verdadeiro irá retornar a palavra sim, mas se a condição for falsa irá retornar não;
console.log("Vanessa" == "Wanessa" ? 'Sim' : 'Não')
console.log(true? 1:2)


