// Keys = é um método que serve para verificar as chaves de um objeto exemplo: 

let obj = {
    'chave1': 1,
    'chave2': 2,
    'chave3': 3,
}

console.log(obj);//aqui irá fazer com que seja apresentado tudo o que há dentro do objeto

console.log(Object.keys(obj));//chamei o objeto pai utilizando o método keys que irá apresentar apenas as chaves criadas dentro desse objeto no console

