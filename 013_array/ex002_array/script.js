//Como acessar um elemento do array pelo seu indice: 

let arr = [1, 4, 5, 6, 7];
let nomes = ['Matheus', 'Vanessa', 'João', 'Aléxia'];
let bool = [true, false, true];

//acessar pelo indice
console.log(arr[0]);//retorna 1
console.log(arr[3]);//retorna 6
console.log(nomes[0]);//retorna Matheus
console.log(nomes[1]);//retorna Vanessa
console.log(bool[0]);//retorna true
console.log(bool[1]);//retorna false
