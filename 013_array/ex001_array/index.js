//Ex 001 - Array 

//Vamos declarar um array com uma sequência numérica 
let arr = [1, 4, 5, 6, 7];//criei uma variavel e atribui o valor de array/conjunto numérico dentro dela 

//Podemos cria arrays de nomes, exemplo: 
let nomes = ['Matheus', 'João', 'Aléxia'];

//Ou até mesmo arrays de booleanos:
let bool = [true, false, true];// Não tem restrição quanto ao tipo de dado 

//E também podemos criar arrays com tipos misturados, exemplo: 
let misturado = [1, 'Matheus', true];

//E também podemos exibir arrays no console: 
console.log(arr);
console.log(nomes);
console.log(bool);
console.log(misturado); 