//indexOf e lastIndexOf

let nums = [5, 6, 2, 4, 9,6,2];//criei um array de números 

console.log(nums.indexOf(6));//solicitei que seja informado onde aparece o primeiro indice que possui o elemento 06 do array, que retornara 1

console.log(nums.lastIndexOf(6));//quando utilizo lastIndexOf eu faço com que o programa aponte qual foi a última posição do elemento 06 no array no caso retornará no informando que foi no indice 05