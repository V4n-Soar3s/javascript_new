//push e pop

let nomes = ['Vanessa', 'Bruna', 'Julia', 'Larissa'];//criei o array

let elementoRemovido = nomes.pop();//solicitei que seja removido o utilmo elemento e dei um nome a ele

console.log(elementoRemovido);//solicitei que seja exibido o elemento removido
console.log(nomes);//solicitei que seja apresentado o array após a remoção do ultimo elemento 

nomes.push('Mariana');//aqui estou inserindo um elemento 
console.log(nomes);//quando for exibido será com o elemento novo Mariana no final 